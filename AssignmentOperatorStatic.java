package LetsLearn;

public class AssignmentOperatorStatic {

public static void main(String[] args) {
//Program for Assignment operator in static way
//Enter a number
int a = 32;
System.out.println("Before assingment operator "+a);
a+=a;//a=a+a
System.out.println("After +=assingment operator "+a);
a-=a;//a=a-a;
System.out.println("After -=assingment operator "+a);
a*=a;//a=a*a;
System.out.println("After *=assingment operator "+a);
a=43;
a/=a;//a=a/a;
System.out.println("After /=assingment operator "+a);
a%=a;//a=a%a;
System.out.println("After +=assingment operator "+a);

}

}